package com.bridgelabz.linecomparisionoops;

public class Points {
	private int x;
	private int y;
	
	public int getXCoordinate() {
		return x;
	}
	public int getYCoordinate() {
		return y;
	}
	
	public int setXCoordinate(int xCoordinate) {
		this.x = xCoordinate;
		return xCoordinate;
	}
	
	public int setYCoordinate(int yCoordinate) {
		this.y = yCoordinate;
		return yCoordinate;
	}

}
